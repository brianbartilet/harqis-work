"""
workflows/testing/tasks/test_farm.py

BDD "test case farm" — chains the Jira board context used by
``workflows/hud/tasks/hud_jira.show_jira_board`` into a Gherkin generation
prompt sent to the Anthropic API and renders a single living markdown file at
``logs/BDD-TEST-FARM.md``.

Flow (RPA, AI-in-the-loop via the Anthropic API):
  1. Fetch the active-sprint Bug/Story tickets from the same Jira Software board
     the HUD reads (statuses: In Review / In Progress / Ready / In Analysis),
     reusing ``ApiServiceJiraBoards.get_board_issues`` + the sprint filter.
  2. For each ticket, fingerprint the tracked Jira fields + description. If the
     fingerprint matches the sidecar state (``logs/.bdd-test-farm.state.json``)
     the previously generated scenarios are reused; otherwise (new or changed
     ticket) scenarios are regenerated by sending a single, self-contained
     Gherkin-generation prompt to the Anthropic API via
     ``BaseApiServiceAnthropic.send_message`` (Sonnet, bounded ``max_tokens``).
  3. Render ``logs/BDD-TEST-FARM.md``: a navigable summary table at the top
     (active tickets only, sorted by issue type then status) plus one section
     per ticket (tracked fields + the generated Gherkin). Tickets that drop out
     of the active focus columns (closed / moved) keep their scenarios under a
     "Retained" group but are removed from the summary table.

Why the API and not the local ``claude`` CLI: this task runs unattended on a
schedule. The Claude Max subscription that backs the ``claude`` CLI is licensed
for interactive Claude Code use, so automated/headless runs must bill the
Anthropic API instead (``ANTHROPIC_API_KEY``). The Gherkin conventions that
the ``/generate-gherkin-scenarios`` skill encodes are inlined into the system
prompt here so the API call is fully self-contained (no skill/tool runtime).
Generation is intentionally token-frugal: a static system prompt carries the
conventions, the per-ticket user message carries only the ticket fields, and
output is capped via ``max_tokens``.

References:
- workflows/hud/tasks/hud_jira.py — board fetch + status/type context (mirrored).
- workflows/finance/tasks/parse_transaction.py — AI-in-the-loop task pattern.
- apps/antropic/references/web/base_api_service.py — the API client used here.
- .claude/skills/generate-gherkin-scenarios/SKILL.md — source of the conventions
  inlined into the system prompt below.
"""

